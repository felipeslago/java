package jse03.operators;

public class TernaryOperator {

	public static void main(String[] args) {
		
		/**
		 * O operador ternário faz a avaliação de uma expressão retornando true ou false
		 * Se for true, executa determinada expressão, se for false executa outra expressão
	 	*/
		
		int a = 1;
		boolean b = a > 0 ? true : false;
		
		System.out.println("Valor de b: " + b);
		
		/**
		 * O primeiro argumento é a condição
		 * Após o operador ternário ? vem a expressão caso a condição seja true
		 * Após o operador : vem a expressão caso a condição seja false
	 	*/
		
	}
	
}
