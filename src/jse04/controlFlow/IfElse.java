package jse04.controlFlow;

public class IfElse {

	public static void main(String[] args) {
		
		/**
		 * As declarações if else são utilizadas para avaliar expressões
        */
		
		int i = 10;
		
		if(i > 0) {
			
			/**
             * Primeira condição
            */
			
		} else if(i < 0) {
			
			/**
             * Outra condição
			 * A declaração else if pode ser utilizada quantas vezes for necessário
			 * Porém, isso não é uma boa prática
            */
			
		} else  {
			
			/**
             * A condição final
            */
			
		}
		
	}
	
}
