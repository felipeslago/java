package jse03.operators;

public class TernaryOperator {

    public static void main(String[] args) {

        /**
         * O operador ternario faz a avaliacao de uma expressao retornando true ou false Se for true, executa
         * determinada expressao, se for false executa outra expressao
         */

        int a = 1;
        boolean b = a > 0 ? true : false;

        System.out.println("Valor de b: " + b);

        /**
         * O primeiro argumento e a condicao Apos o operador ternario ? vem a expressao caso a condicao seja true Apos o
         * operador : vem a expressao caso a condicao seja false
         */

    }

}
